package basics.operator.relational;

public class NotEqualTo {
    public static void main(String[] args) {
        int x = 100, y = 150, z = 100;

        //checking whether x is not equal to y
        System.out.println("x is not equal to y: "+(x!=y));

        //checking whether x is not equal to z
        System.out.println("x is not equal to z: "+(x!=z));
    }
}
